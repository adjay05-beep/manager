-- Create Chat Messages
create table chat_messages (
  id bigint generated by default as identity primary key,
  topic_id bigint references chat_topics(id) not null,
  user_id uuid references profiles(id) not null,
  content text,
  image_url text,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

alter table chat_messages enable row level security;
create policy "Messages are viewable by everyone" on chat_messages for select using (true);
create policy "Authenticated users can insert messages" on chat_messages for insert with check (auth.role() = 'authenticated');

-- Enable Realtime for messages
-- Create Voice Prompts (Keywords for Whisper)
create table voice_prompts (
  id bigint generated by default as identity primary key,
  keyword text not null,
  user_id uuid references profiles(id) not null,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

alter table voice_prompts enable row level security;
create policy "Voice prompts are viewable by everyone" on voice_prompts for select using (true);
create policy "Users can insert their own voice prompts" on voice_prompts for insert with check (auth.role() = 'authenticated');
create policy "Users can delete their own voice prompts" on voice_prompts for delete using (auth.uid() = user_id);

